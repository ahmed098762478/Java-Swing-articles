    private void socTabMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_socTabMouseClicked
        // TODO add your handling code here:

        // TODO add your handling code here:
        int index = socTab.getSelectedRow();
        TableModel model = socTab.getModel();
        String id = model.getValueAt(index, 0).toString();
        SocietePk = Integer.parseInt(id);

        String name = model.getValueAt(index, 1).toString();
        txtname.setText(name);

        String tel = model.getValueAt(index, 2).toString();
        txtTele.setText(tel);

        String rue = model.getValueAt(index, 3).toString();
        txtRue.setText(rue);

        String ville = model.getValueAt(index, 4).toString();
        txtVie.setText(ville);
        String adress = model.getValueAt(index, 5).toString();
        txtadress.setText(adress);

        combo.removeAllItems();

        String fouID = model.getValueAt(index, 6).toString();
        String fouName = model.getValueAt(index, 7).toString();
        combo.addItem(fouID + "-" + fouName);
        try {
            Connection con = DatabaseUtil.getConnection();
            Statement stmt = con.createStatement();

            String sql = "SELECT * FROM Fournisseurs";
            ResultSet rs = stmt.executeQuery(sql);

            while (rs.next()) {
                if (Integer.parseInt(fouID) != rs.getInt(1));
                combo.addItem(rs.getInt("FournisseurID") + "-" + rs.getString("SupplierName"));

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

        btnSave.setEnabled(false);
        btnUpdate.setEnabled(true);


    }//GEN-LAST:event_socTabMouseClicked

