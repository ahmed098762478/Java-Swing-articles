    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String four = (String) combo2.getSelectedItem();
        String fournID[] = four.split("-", 0);
        String prId = (String) combo.getSelectedItem();
        String proid[] = prId.split("-", 0);

        String quan = txtquan.getText();
        String dateStr = txtdate.getText();
        SimpleDateFormat inputFormat = new SimpleDateFormat("dd/MM/yyyy");
        SimpleDateFormat outputFormat = new SimpleDateFormat("yyyy-MM-dd");

        if (validateFields()) {
            JOptionPane.showMessageDialog(null, "all field all requierd");
        } else {
            try {
                Date date = inputFormat.parse(dateStr);
                String formattedDate = outputFormat.format(date);
                Connection con = DatabaseUtil.getConnection();
                // Setting values for the placeholders
                PreparedStatement ps = con.prepareStatement("INSERT INTO Livraisons (DeliveredQuantity  ,DeliveryDate  ,FournisseurID,ProductID ) VALUES ( ?,?, ?, ?)");

                // Setting values for the placeholders
                ps.setString(1, quan);
                ps.setString(2, formattedDate);

                ps.setString(3, fournID[0]);
                ps.setString(4, proid[0]);

                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Livraison added successfully");
                new Livraison().setVisible(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }

    }//GEN-LAST:event_btnSaveActionPerformed

