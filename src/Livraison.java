    private void livreTabMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_livreTabMouseClicked
        // TODO add your handling code here:

        int index = livreTab.getSelectedRow();
        TableModel model = livreTab.getModel();
        String id = model.getValueAt(index, 0).toString();
        livraisonPK = Integer.parseInt(id);

        String quan = model.getValueAt(index, 1).toString();
        txtquan.setText(quan);

        String date = model.getValueAt(index, 2).toString();
        txtdate.setText(date);

        combo2.removeAllItems();
        combo.removeAllItems();

        String fouID = model.getValueAt(index, 3).toString();
        String fouName = model.getValueAt(index, 4).toString();
        combo2.addItem(fouID + "-" + fouName);
        String pID = model.getValueAt(index, 5).toString();
        String pName = model.getValueAt(index, 6).toString();
        combo.addItem(pID + "-" + pName);
        try {
            Connection con = DatabaseUtil.getConnection();
            Statement stmt = con.createStatement();

            String sql = "SELECT * FROM Fournisseurs";
            String sqlu = "SELECT * FROM Product";

            ResultSet rs = stmt.executeQuery(sql);

            while (rs.next()) {
                combo2.addItem(rs.getInt("FournisseurID") + "-" + rs.getString("SupplierName"));
            }

            rs.close();  // Close the first result set before opening the second one

            ResultSet ru = stmt.executeQuery(sqlu);

            while (ru.next()) {
                combo.addItem(ru.getInt("ProductID") + "-" + ru.getString("ArticleName"));
            }

            ru.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

        btnSave.setEnabled(false);
        btnUpdate.setEnabled(true);
    }//GEN-LAST:event_livreTabMouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
        
        
           // TODO add your handling code here:
            int selectedRow = livreTab.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a row to delete.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int option = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this record?", "Confirmation", JOptionPane.YES_NO_OPTION);

        if (option == JOptionPane.YES_OPTION) {
            try {
                // Get the FournisseurID from the selected row
                int fournisseurID = Integer.parseInt(livreTab.getValueAt(selectedRow, 0).toString());

                // Execute the DELETE SQL query
                Connection con = DatabaseUtil.getConnection();

                PreparedStatement ps = con.prepareStatement("DELETE FROM Livraisons  WHERE LivraisonID= ?");
                ps.setInt(1, fournisseurID);
                ps.executeUpdate();

                // Refresh the table data
                DefaultTableModel model = (DefaultTableModel) livreTab.getModel();
                model.removeRow(selectedRow);

                JOptionPane.showMessageDialog(this, "Record deleted successfully.");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }

        
    }//GEN-LAST:event_jButton3ActionPerformed

